              <p class="lt18" style="margin-bottom:-5px;font-size: 1.1em; line-height: 1.2;">Profesor medicinskih znanosti, vodja oddelkov za ortopedijo in travmatologijo vLjubljanskem kliničnem centru, član Slovenskega društva revmatologov in ortopedov, terevropskega društva kirurgov-revmatologov. </p>
