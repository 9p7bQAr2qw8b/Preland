        <p><b class="lt53">-Daljši seznam! Profesor, ali bi lahko opisali svojega povprečnega pacienta? Kakšen je?</b></p>
        <p class="lt54">–Večina mojih bolnikov so navadni ljudje nad 40 let. Vsi so prepričani, da je bolezen prišla s starostjo». Vsi imajo iste diagnoze: osteohondroza, artroza, artritis, radikulitis, stiskanje živca. Vsaka diagnoza moti, poslabša življenje.</p>
        <p class="lt55">-Nekateri se komaj premikajo, medtem ko doživljajo bolečino, zaradi katere iz oči izstopajo solze.. Pred tem so imeli navadno življenje: niso bili niti športniki, niti invalidi. Včasih se sprašujejo – «Kaj sem slabega naredil, da zdaj tole imam? Zakaj to jaz imam?» Jaz odgovarjam, da moramo namesto pritoževanja začeti obnoviti oskrbo s krvjo.</p>
        <p><b class="lt56">-Povejte, kako pomagate ljudem? </b></p>
        <p class="lt57">- Do pred kratkim sem bolnike zdravil tako, da sem jim dajal  96 vaj. To je neverjetno učinkovit, a izredno kompleksen, dolgoročen in drag način. Ne gre za opremo. Za ljudi je to bolečina, delo in za to vedno ni dovolj časa in denarja. Zato sem se odločil najti najboljšo metodo za svoje paciente. In sem ga našel. </p>
        <p><b class="lt58">-Prepričani smo, da bodo te informacije za naše bralce zelo koristne. Ali boste povedali o svoji novi metodi?</b></p>
        <p class="lt59">-Seveda. Ljubljnski inštitut za revmatologijo je bila prva institucija na svetu, ki je prejela certifikat za uporabo inovativnih sredstev za boj proti osteohondrozi in sorodnim boleznim. Odkrito rečeno, ko sem prvič prebral dokumentacijo in rezultate raziskav nove droge, je verjetno bilo slišati moj skeptični smeh. Ti zapisi so mi zdeli nerealni. Toda bi morali videti moj obraz, ko so mi rezultati naših testov pokazali: od 5.000 ljudi, ki so prostovoljno poskusili zdravilo, jih je 4,726 popolnoma odpravilo težave s sklepi. To je več kot 94% od vseh. Ostalih 5,6% je počutilo olajšanje, samo 0,4% je trdilo, da jim ni zdravljenje pomagalo.</p>
        <p><b class="lt60">— Kaj je to zdravilo?</b></p>
        <p class="lt61">- To je posebna krema, ki se imenuje <a href="">Hondrostrong</a>. Neverjetno zdravilo, ki vam omogoča, da v 4 dneh popolnoma pozabite na bolečine v hrbtu in sklepih. Hkrati bo zdravljenje kompleksnejših bolezni trajalo približno mesec dni.</p>
        <p class="lt62">Krema se za zdaj proizvaja le v Kolumbiji, s slovenskim vodstvom in financiranjem. Ministrstvo za zdravstvo je investiralo več kot milijon evrov v razvoj tega zdravila. Potem pa je naš inštitut kupil patent za realizacijo čudežne kreme, plačali smo skoraj isto vsoto </p>
        <p><b class="lt63">- Povejte mi, na kakšen način deluje čudežna krema?</b></p>
        <p class="lt64">- Ne gre za nek čudež, tu je čista znanost. Glavna sestavina kreme je ekstrakt propolisa. Če prodre v prizadeto območje, obnovi vezivo in kostno tkivo.. Si lahko predstavljate, kako zapleten je ta biološki proces? Propolis brez najmanjšega kemičnega dodatka lahko pospeši regeneracijo sklepov, tako da vpliva na cirkulacijski sistem. Sestava vsebuje tudi cedrin terpentin in čebelji vosek za še večjo učinkovitost zdravila. Zaradi številčnosti teh in drugih sestavnih delov je Kolumbija idealen kraj za obsežno proizvodnjo. Naravne sestavine so bile in bodo vedno bolj uporabne kot katera koli kemija. </p>
        <p class="lt65">Samo ena uporaba kreme aktivira več kot 930.000 celic, kar jih vključuje v proces krvnega obtoka. To je ključ do okrevanja. </p>
        <p><b class="lt66">-Sliši se impresivno, vendar bi lahko z enostavnimi besedami pojasnili, kaj to pomeni za običajne ljudi z boleznimi mišično-skeletnega sistema?</b></p>
        <p class="lt67">- To pomeni, da lahko končno pošljemo v muzej vse predhodne metode zdravljenja sklepov. Napredek ne miruje, zdaj pa lahko vsi ozdravijo, ne da bi celo zapustili hiše. Hondrostrong nima učinka hlajenja ali anestezije. Namesto tega se na celičnem nivoju ponovno zažene telo, da bi se borilo proti vzroku bolečine. Z drugimi besedami, bolnik ne le prikrije simptome, ampak popolnoma odpravi bolezen. </p>
        <p class="lt68">Že prvi dan uporabe Hondrostrong se začne proces obnavljanja motoričnih funkcij telesa, bolečina pa se opazno zmanjšuje. Potek zdravljenja običajno traja 2-3 tedne in se konča s popolnim okrevanjem.</p>
        <p><b class="lt69">- Ali to pomeni, da lahko Hondrostrong ozdravi vsako bolezen sklepov?</b></p>
        <p class="lt70">-Ja! Ko se krvni obtok normalizira, se bolnik s katerokoli boleznijo mišično-skeletnega sistema, bodisi hrbta ali sklepov, hitro okreva. Osteohondroza, premik vretenc, motnje, podplutbe, zvini in celo artroza niso več problem. Krema je učinkovita.</p>
