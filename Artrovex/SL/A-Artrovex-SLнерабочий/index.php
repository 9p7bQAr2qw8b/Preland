        <p style="text-align: left;"><strong class="lt13">Pozdravljeni prijatelji!</strong></p>
        <p class="lt14">Pred kratkim se je začel državni program “Zdravi sklepi”, ki je namenjena boju proti boleznim mišično'skeletnega sistema. V sorazmerno kratkem času je omogočil tisočim ljudem, da popolnoma ozdravijo boleče sklepe. Programa se lahko udeleži katerikoli državljan Slovenije, ki se želi znebiti bolečine v sklepih, skoraj brez napora!</p>
