        <p class="lt19">«Samo si zapomnite moje besede in ne poslušaj nikogar drugega: obnoviti zdravje sklepov je možno v vsaki starosti»</p>
        <p class="lt20">Matej Božič trdi, da v samo nekaj mesecih lahko osvobodi pacienta 18-letnega osteofita in da lahko v 78 dneh težek artritis popolnoma pozdravi celo pri starejši osebi. Po besedah profesorja pacienti pozabijo na bolečino po 4 dneh pravilnega zdravljenja. Zdi se fantastično, ampak jeMatej Božič pripravljen potrditi svoje besede z resnimi primeri iz svoje 38'letne prakse.</p>
        <p class="lt21">Takoj po udeležbi v oddaji na RTV (s temo “Zdravi sklepi pri starejših”) se je znani slovenski revmatolog strinjal da bo dal intervju našemu novinarju. </p>
        <p><b class="lt22">– Dober dan, gospod Božič. Pravijo, da sta starost in osteohondroza par za vedno, ali je to res?</b></p>
        <p class="lt23">Pozdravljeni! Seveda to ni res. Tole bom rekel: starost ni povezan z osteohondrozo, ampak je povezan z odvešnim zaupanjem slabim zdravnikom, ki leta in leta zdravijo paciente brez nobenega rezultata. Artroza, hondroza ali katera koli druga bolezen mišično-skeletnega sistema se lahko ozdravi v kateri koli starosti. To je dejstvo, ki je znansveno potrjeno!</p>
        <p class="lt24">Poleg tega, če poznate eno skrivnost in sledite preprostim pravilom, se lahko sklepi zdravijo zelo hitro, tudi če ne zapustite stanovanja. To je kar zdaj počnejo tisoče in tisoče naših pacientov po celi Sloveniji, in v tujini.</p>
        <p><b class="lt25">-Rekli ste skrivnost. Ali jo boste raskrili?</b></p>
        <p class="lt26">-Da bi odpravili težavo, moramo najprej najti njen vir. Če odprete zdravniški priročnik, boste v njej našli do 147 razlogov za razvoj osteohondroze in osteoartritisa z enakimi posledicami - tkivo sklepov izgubi elastičnost in se pojavi bolečina. Preprosto povedano je elastičnost sklepa izgubljena, ko je motena oskrba s krvjo. Po vzpostavitvi normalnega krvnega obtoka v sklepih rešimo problem. To je ta skrivnost! </p>
        <p><b class="lt27">–Da, ampak kako obnovijo oskrbo s krvjo ljudje nad 40 let?</b></p>
        <p class="lt28">-(smeje se) Ta metoda pomaga ljudem pri popolni rehabilitaciji po nesrečah, obupanim ljudem z invalidskimi vozički pa omogoča, da se spet postavijo na noge. Vi pa ste omenili ljudi, ki so starejši kot 40 let. </p>
        <p><b class="lt29">-Kljub temu, če govorimo o starostnih boleznih, kakšni simptomi lahko kažejo ljudem, ki so stari več kot 40 let, da morajo razmišljati o zdravljenju?</b></p>
        <p class="lt30">- Prvi simptomi so: nastajanje škripanja, pordelost, otekanje, prsti se lahko otresejo in seveda vse to spremlja stalna bolečina. V večini primerov se simptomi pojavljajo zaradi stalne preobremenitve, zaradi sedečega načina življenja in pri ljudeh, ki sodelujejo v dviganju uteži. Sčasoma se hrustanec v sklepih izbriše, kar omogoča, da se kosti dotaknejo, tako da čutite nelagodje in bolečine. Če se zdravljenje ne začne pravočasno, bodo simptomi neizogibno postali resni problemi: od vnetja do popolne paralize. Mimogrede je drugi vzrok bolezni lahko povečana raven soli v telesu. V tem primeru, ko čutite nelagodje med gibanjem, morate takoj omejiti porabo izdelkov, ki vsebujejo natrijev klorid, to je, sol. </p>
